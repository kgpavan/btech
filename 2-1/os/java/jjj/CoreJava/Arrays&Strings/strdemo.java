import java.lang.*;

class strdemo {
 public static void main(String args[]) {
String str="Softwarte";
String str1="SOFTWARE";

System.out.println("The length of the string is" + str.length());
System.out.println("The concatenated string are" + str.concat(str1));
System.out.println("The character at a specified location is" + str.charAt(4));
System.out.println("The case conversion is" + str.toUpperCase());
System.out.println("The first occurance of the given character" + str.indexOf('t'));
System.out.println("The last occurance of the given character" + str.lastIndexOf('t'));
System.out.println("The replaced characters are" + str.replace('t','r'));
System.out.println("The case conversion for the second string is" + str1.toLowerCase());
System.out.println("Truncating the additional characters" + str.trim());
System.out.println("Equality of strings are" + str.equals(str1));
System.out.println("Ignoring the equality" + str.equalsIgnoreCase(str1));
   }
}